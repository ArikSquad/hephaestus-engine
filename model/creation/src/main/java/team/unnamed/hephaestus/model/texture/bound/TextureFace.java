package team.unnamed.hephaestus.model.texture.bound;

/**
 * Enum containing all the possible
 * texture UV faces, the constants
 * order is important (ordinals are
 * used to index the texture bounds by
 * faces)
 */
public enum TextureFace {
    NORTH,
    SOUTH,
    WEST,
    EAST,
    UP,
    DOWN
}