package team.unnamed.hephaestus.listener;

import org.bukkit.entity.Player;
import org.bukkit.event.EventHandler;
import org.bukkit.event.Listener;
import org.bukkit.event.player.PlayerQuitEvent;
import team.unnamed.hephaestus.ModelRegistry;
import team.unnamed.hephaestus.view.BukkitModelView;

public class PlayerQuitListener implements Listener {

    private final ModelRegistry modelRegistry;

    public PlayerQuitListener(ModelRegistry modelRegistry) {
        this.modelRegistry = modelRegistry;
    }

    @EventHandler
    public void onQuit(PlayerQuitEvent event) {
        Player player = event.getPlayer();
        for (BukkitModelView view : modelRegistry.views()) {
            view.removeViewer(player);
        }
    }

}
